#include <stdio.h> // printf()

int main()
{
	char st[8];
	st[0] = 'A';
	st[1] = 'B';
	st[2] = 'C';
	st[3] = 'D';
	st[4] = '\0';
	printf("文字列stには\"%s\"が格納されています", st);
	return 0;
}
